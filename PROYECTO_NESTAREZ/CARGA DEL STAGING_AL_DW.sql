INSERT INTO DIM_TIEMPO
(
    ID_FECHA,
    FECHA,
    DIA,
    NOMBRE_DIA,
    MES,
    NOMBRE_MES,
    TRIMESTRE,
    ANIO
)
SELECT DISTINCT

    CONVERT(INT,
        FORMAT(CONVERT(DATE, FECHA_VENTA, 103),'yyyyMMdd')
    ) AS ID_FECHA,

    CONVERT(DATE, FECHA_VENTA,103),

    DAY(CONVERT(DATE, FECHA_VENTA,103)),

    DATENAME(WEEKDAY,CONVERT(DATE, FECHA_VENTA,103)),

    MONTH(CONVERT(DATE, FECHA_VENTA,103)),

    DATENAME(MONTH,CONVERT(DATE, FECHA_VENTA,103)),

    DATEPART(QUARTER,CONVERT(DATE, FECHA_VENTA,103)),

    YEAR(CONVERT(DATE, FECHA_VENTA,103))

FROM STAGING_NESTAREZ.dbo.STG_VENTAS

WHERE FECHA_VENTA IS NOT NULL

AND NOT EXISTS
(
    SELECT 1
    FROM DIM_TIEMPO D
    WHERE D.ID_FECHA =
        CONVERT(INT,
        FORMAT(CONVERT(DATE, FECHA_VENTA,103),'yyyyMMdd'))
);
GO

