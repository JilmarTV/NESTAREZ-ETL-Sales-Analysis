INSERT INTO FACT_VENTAS
(
    ID_FECHA,
    ID_PRODUCTO,
    ID_SUCURSAL,
    CANTIDAD_VENDIDA,
    PRECIO_UNITARIO,
    TOTAL_VENTA
)

SELECT

    DT.ID_FECHA,

    DP.ID_PRODUCTO,

    DS.ID_SUCURSAL,

    CAST(S.CANTIDAD_VENDIDA AS INT),

    CAST(S.PRECIO_UNITARIO AS DECIMAL(10,2)),

    CAST(S.TOTAL_VENTA AS DECIMAL(12,2))

FROM STAGING_NESTAREZ.dbo.STG_VENTAS S

INNER JOIN DIM_TIEMPO DT
ON DT.ID_FECHA =
CONVERT(INT,
FORMAT(CONVERT(DATE,S.FECHA_VENTA,103),'yyyyMMdd'))

INNER JOIN DIM_PRODUCTO DP
ON DP.NOMBRE_PRODUCTO = S.PRODUCTO

INNER JOIN DIM_SUCURSAL DS
ON DS.NOMBRE_SUCURSAL = S.SUCURSAL

WHERE S.ESTADO_STG='OK';
GO
