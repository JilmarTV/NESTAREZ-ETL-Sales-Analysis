INSERT INTO DIM_PRODUCTO
(
    NOMBRE_PRODUCTO,
    CATEGORIA
)
SELECT DISTINCT

    PRODUCTO,
    CATEGORIA

FROM STAGING_NESTAREZ.dbo.STG_VENTAS

